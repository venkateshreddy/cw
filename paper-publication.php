        <h3>Steps that need to be taken care for  your Journal:</h3>
        <p>Preparing  a high-quality research paper that get approval from trusted and top tire journals like IEEE Transaction, Elsevier and Springer publication is a complicated task. As these journals track a strict screening process and need a obvious and significant contribution and a research flavor.</p>
        <p>Most of the students/scholars  usually do a fantastic work during their academic projects but due to lack of understanding and guidance of writing a good research paper make their efforts go unrecognized and appreciated by the research and academic communities.</p>
        <p>There are ten  basic elements of a research paper. They are Title, Abstract, Table of Contents/ Abstract graphics(A scheme or figure to convey the theme of paper), Overview and problem statement, Literature survey, Proposed method, Experimental data analysis and results, Conclusion and References. A good research paper is a combination of these sections, where every section is written as best as it should be.</p>
      


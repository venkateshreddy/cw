<p><strong>Overview and Problem Statement: </strong>This section consists of several paragraphs. This section addresses the problem domain (field or area), its importance and applications. Solving an important problem with good application will usually lead to a good paper. The best practice is to make this section simple clear and easy to understand. If possible try to add references each statement addressing the importance and application.</p>
<p><strong>Literature survey:</strong> This section contains information about the existing methods available to solve the same problem or related problems. A good literature survey usually contains a comprehensive and exhaustive list of methods used in past, their advantages and disadvantages and their applications.</p>
<p><strong>Proposed Method:</strong> This section and results are heart of the paper. Every minute detail about the proposed method need be explained in this section. It is better to writ this section considering that you are writing it for a person who doesn’t know anything about the field and you need to explain him the solution.</p>
<p><strong>Experimental data analysis and results:</strong> This section contains information about experimental data generation or collection and its properties. Application of data to proposed and other existing methods and a deep analysis of result and their validation make this as an ideal section.</p>
<p><strong>Conclusions:</strong> A good conclusion to the paper usually contain two to three paragraphs mentioning the superior properties of the proposed solution compared to existing methods and state its significance in the field with useful applications.</p>
<p><strong>References:</strong> these research papers published in journals or conferences and relevant to the research work being written for the publications. Make sure that the format given by the journal is followed for all references and all of them are cited in the paper.</p>



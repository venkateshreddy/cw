  <p><strong>Abstract:</strong> it gives concise information about the paper. Usually it consist of six to seven short sentences and it needs be with in of 150~200 words. First couple of sentences should focus on what the study is about  A good Abstract includes a statement about problem, proposed solution, and a statement about the datasets and results. A final statement that addresses the contribution of the paper to the relevant field of research. Make this very simple precise and understandable, as readers will decided to read complete paper or not after reading Abstract.
-Be creative in generating curiosity
</p>
<p><strong>Table of Contents/ Abstract graphics:</strong> A scheme or figure to convey the theme of paper. A graphic must be included with each manuscript(a book, document, or piece of music written by hand rather than typed or printed.) for display in the table of contents (TOC) and the abstract. This graphic should capture the reader’s attention and, in conjunction with the manuscript title, should give the reader a quick visual impression of the essence of the manuscript without providing specific results. </p>
